#include<stdio.h>

// FINDING SPACE OCCUPIED BY DIFFERENT DATA TYPES

void main()
{
  printf("No. of bytes occupied by int is %ld\n", sizeof(int));
  printf("No. of bytes occupied by float is %ld\n", sizeof(float));
  printf("NO.of bytes occupied by double is %ld\n", sizeof(double));
  printf("No.of bytes occupied by char is %ld\n", sizeof(char));
}
